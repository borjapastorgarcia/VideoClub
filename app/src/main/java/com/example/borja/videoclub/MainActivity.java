    private void setupViewPager(ViewPager viewPager) {
        PagerAdapter adapter = new PagerAdapter(getSupportFragmentManager());
        adapter.addFragment(new FragmentPeliculas(), "Peliculas");
        adapter.addFragment(new FragmentSeries(), "Series");
        adapter.addFragment(new FragmentMusica(), "Musica");
        viewPager.setAdapter(adapter);
    }
